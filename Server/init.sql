CREATE TABLE PLAYERS(
    ID INTEGER PRIMARY KEY   AUTOINCREMENT,
    NAME TEXT,
    LEVEL INT,
    SCORE INT,
    FLAT INT
);

CREATE TABLE FLATS(
    ID INTEGER PRIMARY KEY   AUTOINCREMENT,
    URL TEXT,
    OBJECTS ENUM('A', 'B', 'C'),
    POS ENUM('X', 'Y', 'Z', 'T', 'U', 'V'),
    CIRCUITS ENUM('A', 'B', 'C')
);

CREATE TABLE CIRCUITS(
    ID INTEGER PRIMARY KEY   AUTOINCREMENT,
    URL TEXT,
    OBJECTS ENUM('A', 'B', 'C'),
    POS ENUM('X', 'Y', 'Z', 'T', 'U', 'V'),
);


